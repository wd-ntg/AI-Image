import mongoose from "mongoose";

const Auth = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    avatar: {
        type: String,
        required: false
    }
})

const AuthSchema = mongoose.model("Auth", Auth)

export default AuthSchema